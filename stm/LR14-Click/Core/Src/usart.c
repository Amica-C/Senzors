HAL_DMA_StateTypeDef Uart_Info(const char *strInfo)
{
	HAL_DMA_StateTypeDef ret = HAL_ERROR;
	if (strInfo != NULL)
		ret = HAL_UART_Transmit(&huart1, (const uint8_t*) strInfo, strlen(strInfo), 10000);
	return ret;
}

HAL_DMA_StateTypeDef Uart_StartReceving() //
{
	uart_data_ready = 0;
	uart_req_w_ptr = uart_req_buf;
	memset(uart_req_buf, 0, sizeof(uart_req_buf));
	return Uart_ContinueReceiving();
}

HAL_DMA_StateTypeDef Uart_ContinueReceiving() //
{
	HAL_StatusTypeDef halStatus = HAL_UART_Receive_IT(&huart1, (uint8_t*) uart_req_w_ptr, 1);
	if (halStatus != HAL_OK && halStatus != HAL_BUSY) //
	{
		char sError[50];

		sprintf(sError,"HAL_UART_Receive_IT failed:%d,%d", (int)huart1.ErrorCode, (int)halStatus);
		Uart_Info(sError);

		Error_Handler();
	}
	return halStatus;
}

/*
 * brief Uart reading data, if \n is commit, the buffer is ready to send via usb
 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) //
{
	if (huart->Instance == huart1.Instance) //
	{
		if (*uart_req_w_ptr == '\n' || *uart_req_w_ptr == '\r'
				|| uart_req_w_ptr == uart_req_buf + sizeof(uart_req_buf) - 2) //
		{
			uart_data_ready = 1; // data ready, UART is stopped, until data are send
		} //
		else //
		{
			uart_req_w_ptr++;
			Uart_ContinueReceiving();
		}
	}
}

